package net.tackley.dbdeploy.scripts;

public class UnrecognisedFilename extends Exception {

	private static final long serialVersionUID = 1L;

	public UnrecognisedFilename() {
		super();
		
	}

	public UnrecognisedFilename(String message, Throwable cause) {
		super(message, cause);
		
	}

	public UnrecognisedFilename(String message) {
		super(message);
		
	}

	public UnrecognisedFilename(Throwable cause) {
		super(cause);
		
	}
	
	

}
